/**
 * Classe Abstraite Expression
 */

public abstract class Expression{
/**
    *Cette fonction permet d'accéder à la valeur d'une expression.
    *
    *Elle doit être implémenter dans les classes fille de Expression.
    *
    * Elle retourne un nombre décimal.
    *
    *@return un double 
 */
    
    public abstract double valeur();
} 
